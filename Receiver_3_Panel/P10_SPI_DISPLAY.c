void setPixel(uint8_t x, uint8_t y, uint8_t state) {
  if (y >= P10_WIDTH || x >= P10_HEIGHT) return;
  uint8_t col = y / 8;
  uint8_t bit = y % 8;
  if (state) frameBuffer[x][col] |= (1 << bit);
  else frameBuffer[x][col] &= ~(1 << bit);
}
void P10_refresh() {
  for (uint8_t row = 0; row < 4; row++) {
    digitalWrite(P10_A, !(row & 0x01));
    digitalWrite(P10_B, !((row >> 1) & 0x01));

    int col = 4;
    for(int i = 2; i >=0; i--){ 

    for (uint8_t row2 = 0; row2 < 4; row2++) {
      SPI_transfer(frameBuffer[row2 * 4 + row][col*i + 3]);
            // SPI_transfer(0x00);
    }
    // SPI_transfer(0xff);
    // SPI_transfer(0xff);
    for (uint8_t row2 = 0; row2 < 4; row2++) {
      SPI_transfer(frameBuffer[row2 * 4 + row][col*i + 2]);
    }
    for (uint8_t row2 = 0; row2 < 4; row2++) {
      SPI_transfer(frameBuffer[row2 * 4 + row][col*i + 1]);
    }
    for (uint8_t row2 = 0; row2 < 4; row2++) {
      SPI_transfer(frameBuffer[row2 * 4 + row][col*i + 0]);
    }
}
    digitalWrite(P10_LAT, HIGH);
    digitalWrite(P10_LAT, LOW);

    digitalWrite(P10_ENABLE, HIGH);
    delayMicroseconds(200);
    digitalWrite(P10_ENABLE, LOW);
  }
}

void P10_draw_digit(int digit, int x, int y) {
  if (digit < 0 || digit > 9) return;
  for (int row = 0; row < 16; row++) {
    uint16_t rowData = font_10x16_digits[digit][row];
    for (int col = 0; col < 10; col++) {
      bool pixelOn = rowData & (1 << (9 - col));
      setPixel(x + row, y + col, !pixelOn);
    }
  }
}

void P10_display_number(int number) {
  number = constrain(number, 0, 999);
  int hundreds = number / 100;
  int tens = (number % 100) / 10;
  int ones = number % 10;

  P10_clear();
  P10_draw_digit(hundreds, 0, 0);
  P10_draw_digit(tens, 0, 11);
  P10_draw_digit(ones, 0, 22);
  P10_refresh();
}

// Display number on a specific panel (0-2)
void P10_display_number_zone(uint8_t panel, int number) {
  if (panel >= NUM_PANELS) return;

  int x_offset = 0;
  int y_offset = panel * PANEL_WIDTH;

  number = constrain(number, 0, 999);
  int hundreds = number / 100;
  int tens     = (number % 100) / 10;
  int ones     = number % 10;

  // Clear only this panel area
  for (int x = 0; x < P10_HEIGHT; x++) {
    for (int y = y_offset; y < y_offset + PANEL_WIDTH; y++) {
      setPixel(x, y, 1); // blank (since pixels inverted)
    }
  }

  // Draw digits inside this zone
  P10_draw_digit(hundreds, x_offset, y_offset + 0);
  P10_draw_digit(tens,     x_offset, y_offset + 11);
  P10_draw_digit(ones,     x_offset, y_offset + 22);
}