void P10_init() {
  pinMode(P10_ENABLE, OUTPUT);
  pinMode(P10_A, OUTPUT);
  pinMode(P10_B, OUTPUT);
  pinMode(P10_LAT, OUTPUT);
  digitalWrite(P10_ENABLE, LOW);
  P10_clear();
}

void P10_clear() {
  memset(frameBuffer, 0xFF, sizeof(frameBuffer));
}

